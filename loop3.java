public class loop3 {
    public static void main(String[] args) {
        System.out.println("even numbers using while loop : ");
        int i=2;
        while(i<=10){
            System.out.println(i);
            i=i+2;
        }
        System.out.println("odd numbers using while loop : ");
        int j=1;
        while (j<=10) {
            System.out.println(j);
            j=j+2;
        }
    }
}
