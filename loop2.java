public class loop2 {
    public static void main(String[] args) {
        System.out.println("even numbers from 0 to 10 : ");
        for(int i=2;i<=10;i=i+2){
            System.out.println(i);
        }
        System.out.println("odd numbers from 0 to 10 : ");
        for(int i=1;i<=10;i=i+2){
            System.out.println(i);
        }
    }
}
